    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
       // Seta os dados da tela no modelo de produto
        String auxCodigonota = edtCodigo.getText();
        String auxCodigoprofessor = edtcodigoprofessor.getText();
        String auxCodigoaluno = edtcodigoaluno.getText();
        String auxData = edtData.getText();
        String auxCodigomateria = edtcodigomateria.getText();
        String auxNota = edtNota.getText();

        // valida campos vazios
        if(validaCamposVazios(auxCodigonota,auxCodigoprofessor,auxCodigoaluno,auxData,auxCodigomateria,auxNota )){
            int codigonota = Integer.parseInt(auxCodigonota);
            int codigoprofessor = Integer.parseInt(auxCodigoprofessor);
            int codigoaluno = Integer.parseInt(auxCodigoaluno);
            int codigomateria = Integer.parseInt(auxCodigomateria);
            double Notas = Double.parseDouble(auxNota);

            String auxDia = auxData.substring(0,2);
            String auxMes = auxData.substring(3,5);
            String auxAno = auxData.substring(6,10);

            int ano = Integer.parseInt(auxAno);
            int mes = Integer.parseInt(auxMes);
            int dia = Integer.parseInt(auxDia);
            
            ano = ano - 1900;
            mes = mes - 1;

            java.sql.Date dataNota = new java.sql.Date(ano,mes,dia);

            ModelNota notas = new ModelNota(codigoprofessor, codigomateria, codigoaluno, codigonota, dataNota, Notas);

            ModelNota notaBanco = notadb.getNota(codigonota);
            if (notaBanco.getCodigonota() > 0) {
                if(notadb.gravarAlteracaoNotas(notas)){
                    JOptionPane.showMessageDialog(null, "Nota alterada com sucesso!");

                    limpaCampos();

                    habilitaCampos(false);

                    edtCodigo.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao gravar alteraÃ§Ã£o da nota!");
                }
            } else {
                if(notadb.gravarInsercaoNota(notas)){
                    JOptionPane.showMessageDialog(null, "Nota inserida com sucesso!");

                    limpaCampos();

                    habilitaCampos(false);

                    edtCodigo.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao inserir nota!");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Existem campos nÃ£o preenchidos!");
        }
    }//GEN-LAST:event_btnGravarActionPerformed

