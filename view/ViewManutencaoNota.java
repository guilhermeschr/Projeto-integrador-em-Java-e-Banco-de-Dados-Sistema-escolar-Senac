    private void edtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edtCodigoKeyPressed
        if ((evt.getKeyCode() == KeyEvent.VK_ENTER) || (evt.getKeyCode() == KeyEvent.VK_TAB)) {
            if (edtCodigo.getText().equals("")) {

                edtCodigo.setText(String.valueOf(notadb.getUltimoCodigo("tbnota", "codigo")));

                habilitaCampos(true);

                edtCodigo.requestFocus();
            } else {
                int codigonota = Integer.parseInt(edtCodigo.getText());
//                ModelNota  = new ModelNota();
                ModelNota notaBanco = notadb.getNota(codigonota);
                if (notaBanco.getCodigonota() > 0) {
                    //edtcodigoaluno.setText(notaBanco.getcodigoaluno());
                     String auxcodprofessor = "" + notaBanco.getCodigoprofessor();
                    edtcodigoprofessor.setText(auxcodprofessor);
                     String auxcodmateria = "" + notaBanco.getCodigomateria();
                    edtcodigomateria.setText(auxcodmateria);
                     String auxcodaluno = "" + notaBanco.getCodigoaluno();
                    edtcodigoaluno.setText(auxcodaluno);
                    String auxdata = "" + notaBanco.getData();
                    
                    // formatar a data do banco 
                     
                    LocalDate hoje = LocalDate.now();

                    String auxData = "" + auxdata;
                    String auxAno = auxData.substring(0,4);
                    String auxMes = auxData.substring(5,7);
                    String auxDia = auxData.substring(8,10);

                    String auxNovaData = auxDia + "/" + auxMes + "/" + auxAno;
                    
                    edtData.setText(auxNovaData);
                    
                    String auxnota = "" + notaBanco.getNota();
                    edtNota.setText(auxnota);

                    habilitaCampos(true);
                    edtcodigoaluno.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "nota nÃ£o existe!");

                    habilitaCampos(false);

                    edtCodigo.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_edtCodigoKeyPressed

